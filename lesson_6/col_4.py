'''
Буква "a" стоит 10 очков, "b" - 20, "c" - 30, "d" - 40
Запросить кодовою фразу из пяти символов используя только a, b, c, d.
Вывести на экран общее количество очков введенной фразы.

'''

# Стоимость букв
points = {
    'a': 10,
    'b': 20,
    'c': 30,
    'd': 40
}

print("Буква 'a' = 10 очков, 'b' = 20, 'c' = 30, 'd' = 40")
print("Введите кодовую фразу из 5 символов (только a, b, c, d):")


while True:
    phrase = input("Фраза: ").strip().lower()

    if len(phrase) != 5:
        print("❌ Ошибка: фраза должна содержать ровно 5 символов.")
        continue


    if all(char in points for char in phrase):
        break
    else:
        print("❌ Ошибка: разрешены только буквы: a, b, c, d.")

# Подсчёт очков
total = sum(points[char] for char in phrase)

# Вывод результата
print(f"\n✔ Кодовая фраза: {phrase}")
print(f"Общее количество очков: {total}")
